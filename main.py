import random
import time
import re

request = input().lower().strip()


def get_time():
    t = time.localtime()
    return str(t.tm_hour) + ':' + str(t.tm_min)


int_expr = "[+-]?\\b[0-9]+\\b"
math_expr = "\\d+[\\+\\-\\*/]\\d+"


def rand_int(s):
    a, b = re.findall(re.compile(int_expr), s)
    return random.randrange(int(a), int(b) + 1)


def calc(s):
    return eval(re.findall(re.compile(math_expr), s)[0])


