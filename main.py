    "— Дедушка, а куда ты гонишь 200 км/ч?\n— К бабушке, внучек.",
    "— О боже! Что у тебя с фигурой?!\n— Ну, у меня двое детей... \n— И что? Ты их сожрала?!..",
    "Уборщица на Титанике в последние 20 минут задолбалась тряпку выжимать",
